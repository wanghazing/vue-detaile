export function getAllComponentsByPage(context) {
  // console.log(context);
  let urlArr = context.url.split("?");
  let params;
  if (urlArr.length > 1) {
    // console.log();
    params = queryString.parse(urlArr[1]);
    return {
      code: "00000000",
      data: {
        list: componentList.filter(({ page }) => page === params.page),
      },
      message: "ok",
    };
  } else {
    return {
      code: "00000010",
      data: {},
      message: "参数错误",
    };
  }
}
